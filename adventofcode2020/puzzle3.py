def gettrees(map, x, y):
    width = map[0].__len__()
    height = map.__len__()

    #print("width " + str(width))
    #print("height " + str(height))

    n = 0
    prev = 0
    num_trees = 0
    while n < height - x:
        n += x
        prev = prev + y
        #print("n" + str(n))
        #print("prev" + str(prev))
        c = map[n][prev % (width)]
        if c == '#':
            #print("TREE")
            num_trees += 1
            #print("num_trees" + str(num_trees))

    print(num_trees)
    return num_trees

